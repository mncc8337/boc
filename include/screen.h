class Screen {
public:
    void process_navigation(
        int button_up,
        int button_down,
        int button_select,
        int &button_up_clicked,
        int &button_down_clicked,
        int &button_select_clicked
    );
    virtual void draw(U8G2 &u8g2) = 0;
    virtual void set_item_actions(Screen **_item_actions) = 0;
    virtual Screen *get_item_action() = 0;
    virtual void process_navigation(
        int button_up,
        int button_down,
        int &button_up_clicked,
        int &button_down_clicked
    ) = 0;
    virtual bool is_menu() {
        return false;
    }
};

class Menu: public Screen {
private:
    const char **menu_items;
    Screen **item_actions;
    unsigned item_count;

    int item_selected = 0;
    int item_sel_previous;
    int item_sel_next;

public:
    Menu(const char **_menu_items, unsigned _item_count) {
        menu_items = _menu_items;
        item_count = _item_count;
    }

    void set_item_actions(Screen **_item_actions) {
        item_actions = _item_actions;
    }

    Screen *get_item_action() {
        return item_actions[item_selected];
    }

    void process_navigation(
        int button_up,
        int button_down,
        int &button_up_clicked,
        int &button_down_clicked
    ) {
        if((digitalRead(button_up) == HIGH) && (button_up_clicked == 0)) {
            item_selected = item_selected - 1;
            button_up_clicked = 1;
            if(item_selected < 0) {
                item_selected = this->item_count - 1;
            }
        } else if((digitalRead(button_down) == HIGH) && (button_down_clicked == 0)) {
            item_selected = item_selected + 1;
            button_down_clicked = 1;
            if(item_selected >= item_count) {
                item_selected = 0;
            }
        } 

        if((digitalRead(button_up) == LOW) && (button_up_clicked == 1)) {
            button_up_clicked = 0;
        }
        if((digitalRead(button_down) == LOW) && (button_down_clicked == 1)) {
            button_down_clicked = 0;
        }

        item_sel_previous = item_selected - 1;
        if(item_sel_previous < 0)
            item_sel_previous = item_count - 1;
        item_sel_next = item_selected + 1;  
        if(item_sel_next >= item_count)
            item_sel_next = 0;
    }

    void draw(U8G2 &u8g2) {
        u8g2.clearBuffer();

        // selected item background
        u8g2.drawXBMP(0, 22, 128, 21, BITMAP_ITEM_SEL_OUTLINE);

        // draw previous item as icon + label
        u8g2.setFont(u8g2_font_7x14_tr);
        u8g2.drawStr(25, 15, menu_items[item_sel_previous]); 
        // u8g2.drawXBMP( 4, 2, 16, 16, bitmap_icons[item_sel_previous]);          

        // draw selected item as icon + label in bold font
        u8g2.setFont(u8g2_font_7x14B_tr);    
        u8g2.drawStr(25, 15+20+2, menu_items[item_selected]);   
        // u8g2.drawXBMP( 4, 24, 16, 16, bitmap_icons[item_selected]);     

        // draw next item as icon + label
        u8g2.setFont(u8g2_font_7x14_tr);     
        u8g2.drawStr(25, 15+20+20+2+2, menu_items[item_sel_next]);   
        // u8g2.drawXBMP( 4, 46, 16, 16, bitmap_icons[item_sel_next]);  

        // draw scrollbar background
        u8g2.drawXBMP(128-8, 0, 8, 64, BITMAP_SCROLLBAR_BACKGROUND);

        // draw scrollbar handle
        u8g2.drawBox(125, 64/this->item_count * item_selected, 3, 64/this->item_count); 

        u8g2.sendBuffer(); 
    }

    bool is_menu() {
        return true;
    }
};

